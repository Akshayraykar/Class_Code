class SwitchDemo{

	public static void main(String[]args){

		int x =2;

		switch(x){

			default:
				System.out.println("power");
				break;

			case 1:
				System.out.println("one");
				break;

			case 2:
				System.out.println("two");
				break;

			case 3:
				System.out.println("three");
				break;

			
		}
		System.out.println("no output");

	}
}
